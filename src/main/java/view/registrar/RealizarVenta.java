    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        if (model.getRowCount() > 0) {
            if (!clienteData.isEmpty()) { //se verifica que se haya seleccionado un cliente
                if (confirmarVenta()) {
                    VentaVo venta = new VentaVo();
                    cargarDatosVenta(venta);
                    if (ventaCon.registrarVenta(venta)) {
                        JOptionPane.showMessageDialog(this, "Venta registrada correctamente");
                        RealizarVenta reVenta = new RealizarVenta();
                        reVenta.setLocationRelativeTo(null);
                        reVenta.setVisible(true);
                        this.dispose();

                    } else {
                        JOptionPane.showMessageDialog(this, "Error al registrar la venta", "Aviso", JOptionPane.ERROR_MESSAGE);
                    }
                }
            } else {
                JOptionPane.showMessageDialog(this, "Debe seleccionar un cliente", "Aviso", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Debe agregar productos", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnConfirmarActionPerformed

    private void tblFacturaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblFacturaMouseClicked
        int colSeleccionada = tblFactura.getSelectedColumn();
        if (colSeleccionada == 3) {
            int filaSeleccionada = tblFactura.getSelectedRow();
            double cost_uni = Double.parseDouble(model.getValueAt(filaSeleccionada, 2).toString());
            double cantidad = Double.parseDouble(JOptionPane.showInputDialog("Cantidad a comprar"));

            if (cantidad > 0) {
                double total = cost_uni * cantidad;
                model.setValueAt((int) cantidad, filaSeleccionada, 3);
                model.setValueAt((int) total, filaSeleccionada, 4);
            } else {
                model.removeRow(filaSeleccionada);
            }

            calcularTotal();
        }
    }//GEN-LAST:event_tblFacturaMouseClicked

